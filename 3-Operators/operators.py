# 1. Arithmetic Operators: perform common mathematical operation ---
"""
+	    Addition	        x + y	
-	    Subtraction	      x - y	
*	    Multiplication	  x * y	
/	    Division	        x / y	
%	    Modulus	          x % y	
**    Exponentiation	  x ** y	
//    Floor division	  x // y

"""

######################################################################################
# 2. Assignment Operators: used to assign values to variables ---

"""
=	    x = 5	    x = 5	
+=	  x += 3	  x = x + 3	
-=	  x -= 3	  x = x - 3	
*=	  x *= 3	  x = x * 3	
/=	  x /= 3	  x = x / 3	
%=	  x %= 3	  x = x % 3	
//=	  x //= 3	  x = x // 3	
**=	  x **= 3	  x = x ** 3	
&=	  x &= 3	  x = x & 3	
|=	  x |= 3	  x = x | 3	
^=	  x ^= 3	  x = x ^ 3	
>>=	  x >>= 3	  x = x >> 3	
<<=	  x <<= 3	  x = x << 3

"""

######################################################################################
# Comparison Operators: used to compare two values

"""
==	  Equal	                     x == y	
!=	  Not equal	                 x != y	
>	    Greater than	             x > y	
<	    Less than	                 x < y	
>=	  Greater than or equal to	 x >= y	
<=	  Less than or equal to	     x <= y

"""

######################################################################################
#  Logical Operators: used to combine conditional statements
"""
and: 	Returns True if both statements are true.	
x < 5 and  x < 10	

or:	Returns True if one of the statements is true.
x < 5 or x < 4	

not:	Reverse the result, returns False if the result is true.
(x < 5 and x < 10)
"""